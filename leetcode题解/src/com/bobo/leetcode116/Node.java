package com.bobo.leetcode116;

/**
 * @author by bobo
 * @Description Definition for a Node
 * @Date 2022/9/9 10:30 AM
 */
public class Node {
    public int val;
    public Node left;
    public Node right;
    public Node next;

    public Node() {
    }

    public Node(int _val) {
        val = _val;
    }

    public Node(int _val, Node _left, Node _right, Node _next) {
        val = _val;
        left = _left;
        right = _right;
        next = _next;
    }
}
